    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            String carrera = jcbCarrera.getSelectedObjects().toString();
            String nombreAlumno = txtNombreAlumno.getText();
            String apellidoAlumno = txtApellidoAlumno.getText();
            int rutAlumno = Integer.parseInt(txtRutAlumno.getText());
            char dvAlumno = txtDVAlumno.getText().charAt(0);
            Alumno alumno = new Alumno(carrera, nombreAlumno, apellidoAlumno, rutAlumno, dvAlumno);
            String nombreProfe = txtNombreProfesor.getText();
            String apellidoProfe = txtApellidoProfesor.getText();
            int rutProfe = Integer.parseInt(txtRutProfesor.getText());
            char dvProfe = txtDVProfesor.getText().charAt(0);
            Profesor profesor = new Profesor(nombreProfe, apellidoProfe, rutProfe, dvProfe);
            String nombreAsignatura = txtNombreAsignatura.getText();
            String sala = txtSala.getText();
            Asignatura asignatura = new Asignatura(nombreAsignatura, sala, alumno, profesor);
            coleccion.add(asignatura);
            JOptionPane.showMessageDialog(null, "Guardado con Exito");
            Limpiar();
            CargarGrilla();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Guardar: ");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

