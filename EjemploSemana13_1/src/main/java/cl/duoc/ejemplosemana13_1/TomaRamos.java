    public void CargarGrilla(){
        mt.setRowCount(0); //Limpia la grilla
        for (Asignatura asignatura : coleccion) {
            mt.addRow(new Object[]{
                asignatura.getNombreAsignatura(),
                asignatura.getAlumno().getNombre(),
                asignatura.getAlumno().getApellido(),
                asignatura.getSala()
            });
        }
    }
    
