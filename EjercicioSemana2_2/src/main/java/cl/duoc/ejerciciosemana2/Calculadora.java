public class Calculadora {
    private int numero1;
    private int numero2;

    public Calculadora() {
    }
    
    public int suma(int numero1, int numero2){ 
        return numero1 + numero2;
    }
    
    public int resta(int numero1, int numero2){ 
        return numero1 - numero2;
    }
    
    public double dividir(int numero1, int numero2){ 
        return numero1 / numero2;
    }
    
}
